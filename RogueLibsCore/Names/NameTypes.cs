namespace RogueLibsCore
{
    /// <summary>
    ///   <para>The collection of name types used in the game. Use it to avoid typos.</para>
    /// </summary>
    public static class NameTypes
    {
        /// <summary>
        ///   <para>The name type for the names of items and abilities.</para>
        /// </summary>
        public const string Item = "Item";
        /// <summary>
        ///   <para>The name type for the descriptions of items, abilities, traits status effects, and agents.</para>
        /// </summary>
        public const string Description = "Description";
        /// <summary>
        ///   <para>The name type for the names of traits and status effects.</para>
        /// </summary>
        public const string StatusEffect = "StatusEffect";
        /// <summary>
        ///   <para>The name type for the interface buttons, labels and other text.</para>
        /// </summary>
        public const string Interface = "Interface";
        /// <summary>
        ///   <para>The name type for the names and descriptions of mutators and big quests.</para>
        /// </summary>
        public const string Unlock = "Unlock";
        /// <summary>
        ///   <para>The name type for the names of objects and chunk types.</para>
        /// </summary>
        public const string Object = "Object";
        /// <summary>
        ///   <para>The name type for the names of agents.</para>
        /// </summary>
        public const string Agent = "Agent";
        /// <summary>
        ///   <para>The name type for the agent dialogues.</para>
        /// </summary>
        public const string Dialogue = "Dialogue";
    }
}
